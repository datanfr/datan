      <h2 class="mt-4">En quelques mots</h2>
      <p>
        <b>Datan</b> est un outil indépendant visant à <b>mieux rendre compte de l’activité parlementaire des députés</b>. Pour ce faire, l’accent est mis sur les pratiques de vote, permettant ainsi aux citoyens de comprendre le positionnement réel de leurs parlementaires.
      </p>
      <h2 class="mt-4">Le contexte</h2>
      <p>
        Les citoyens <b>méconnaissent le travail et les positions de leur député</b>. Bien qu’ils élisent leurs représentants tous les cinq ans, ils sont peu informés quant à leurs positions de vote et maîtrisent souvent mal leurs profils politiques et leurs affinités avec d’autres groupes parlementaire.
      </p>
      <p>
        La tradition politique française a depuis longtemps délégué le travail législatif aux représentants du peuple. Mais pour que cette démocratie représentative fonctionne, les citoyens doivent pouvoir juger, même a posteriori, les positions de leur député. Sans cette information, les parlementaires ne sont pas incités à rendre des comptes, et les citoyens ne votent pas forcément pour le parti pour lequel ils sont le plus proches.
      </p>
      <p>
        Datan vise donc à fournir aux citoyens et acteurs de l'espace public (société civile, médias) une information accessible relative aux votes et positions politiques des parlementaires.
      </p>
      <p>
        Pour plus d'information sur nos statistiques, <a href="<?= base_url() ?>statistiques">cliquez ici</a>.
      </p>
      <h2 class="mt-4">Conseil scientifique</h2>
      <p><b>Datan</b> s'est doté d'un conseil scientifique en novembre 2020. Celui-ci est composé de six universitaires, français et étrangers, spécialistes des études législatives.</p>
      <p>Le conseil, présidé par Olivier Costa, vise à garantir la validité scientifique et l’éthique des choix méthodologiques opérés pour la constitution de la base de donnée, sa mise en ligne et son exploitation. Il veille également à la neutralité du site vis-à-vis des organisations politiques et des institutions parlementaires.</p>
