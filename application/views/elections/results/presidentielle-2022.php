<h2 class="my-4">Résultats de l'élection présientielle de 2022</h2>
<p>L'élection présidentielle de 2022 s'est déroulée les dimanches 10 et 24 avril.</p>
<p>Au total, <b>douze candidats</b> ont été validées par le Conseil constitutionnel. Au total, 530 députés <a href="<?= base_url() ?>parrainages-2022" target="_blank">ont décidé de parrainer un candidat</a>.</p>
<p>Au premier tour, <b>Emmanuel Macron</b> (En Marche) est arrivé en tête avec 28 % des suffrages, suivi de <b><a href="<?= base_url() ?>deputes/pas-de-calais-62/depute_marine-lepen" target="_blank">Marine Le Pen</a></b> (Rassemblement national), avec 23 % des suffrages.</p>
<p>Ils ont été suivis de <a href="<?= base_url() ?>deputes/bouches-du-rhone-13/depute_jeanluc-melenchon" target="_blank">Jean-Luc Mélenchon</a> (La France insoumise) et d'Éric Zemmour (Reconquête). Les deux candidats arrivés en tête sont sélectionnés pour le second tour.</p>
<p>Au second tour, <b>Emmanuel Macron</b> a été élu président avec 59 % des suffrages exprimés.</p>
